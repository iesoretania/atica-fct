namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity
 */
class NonSchoolDay
{
    /**
     * @ORM\Id
     * @ORM\Column(type="date")
     * @var \DateTime
     */
    protected $date;

    /**
     * @ORM\Column(type="string")
     * @var string
     */
    protected $name;

    /**
     * Set date
     *
     * @param \DateTime $date
     *
     * @return NonSchoolDay
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get date
     *
     * @return \DateTime
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return NonSchoolDay
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
